type parseAbleMessage struct {
	fields    []fieldType
	minLength uint
}

type fieldType struct {
	name        string
	typ         string
	width       int
	embedded    bool
	isArray     bool
	arrayLength int
}

func main() {
	flag.Parse()
	log.Println("Working on", *inputFile)
	if *outputDir == "" {
		var err error
		*outputDir, err = os.Getwd()
		if err != nil {
			panic(err)
		}
	}
	log.Println("output files go in", *outputDir)

	//contents, err := ioutil.ReadFile(*inputFile)
	//if err != nil {
	//	panic(err)
	//}
	fset := token.NewFileSet()
	f, err := parser.ParseFile(fset, *inputFile, nil, parser.ParseComments)
	if err != nil {
		panic(err)
	}
	output := `
// WARNING: this file is generated by parser_generator/main.go do not edit directly.
package ais

`
	for _, decl := range f.Decls {
		// if it is a struct
		st, ok := decl.(*ast.GenDecl)
		if !ok {
			continue
		}
		if st.Tok == token.TYPE {
			for _, spec := range st.Specs {
				typespec, ok := spec.(*ast.TypeSpec)
				if !ok {
					continue
				}
				structType, ok := typespec.Type.(*ast.StructType)
				if !ok {
					continue
				}
				name := typespec.Name.Name
				if !unicode.IsUpper(rune(name[0])) {
					continue
				}
				// print out function for parsing this type..

				log.Println("Found type to generate: ", name)

				var minLength = uint(0)
				fields := []fieldType{}
				for _, field := range structType.Fields.List {
					if len(field.Names) == 0 {
						// it's probably the header embed or communicationStateNoItdma
						fields = append(fields, fieldType{
							embedded: true,
							typ:      field.Type.(*ast.Ident).Name,
						})
						continue
					}
					fieldName := field.Names[0].Name
					var typ string
					var isArray bool
					var arrayLength int
					switch msg := field.Type.(type) {
					case *ast.Ident:
						typ = msg.Name
					case *ast.ArrayType:
						var length string
						isArray = true
						if msg.Len != nil && msg.Len.(*ast.BasicLit).Kind == token.INT {
							length = msg.Len.(*ast.BasicLit).Value
							arrayLength, err = strconv.Atoi(length)
							if err != nil {
								panic(err)
							}
						}
						typ = msg.Elt.(*ast.Ident).Name
					}
					if field.Tag == nil {
						continue
					}
					tags := strings.Split(field.Tag.Value, " ")
					var width int
					for _, tag := range tags {
						parts := strings.Split(tag, ":")
						if len(parts) > 2 {
							panic("unepxected multiple parts to tag")
						}
						//I.E aiswidth:6
						if parts[0] == "aiswidth" {
							width, err = strconv.Atoi(parts[1])
							if err != nil {
								panic(err)
							}
							minLength += uint(width)
						} else if parts[0] == "aisDependsBit" {

						} else if parts[0] == "aisoptional" {
							// TODO support this...
							// optional = true
						} else if parts[0] == "aisEncodeMaxLen" {
							ml, err := strconv.Atoi(parts[1])
							if err != nil {
								panic(err)
							}
							if ml < 0 {
								panic("impossible to have a negative aisEncodeMaxLen but got " + parts[1])
							}
							minLength = uint(ml)
						}

					}
					log.Println("\t", fieldName, typ, tags, width)
					fields = append(fields, fieldType{
						name:        fieldName,
						typ:         typ,
						width:       width,
						arrayLength: arrayLength,
						isArray:     isArray,
					})
				}
				output += `func parse` + name + `(payload []byte, t *Codec, offset *uint) *` + name + ` {
	p := &` + name + `{}
// TODO if it has a valid field then write it here...
	var optional bool
	minLength := uint(` + strconv.Itoa(int(minLength)) + `)
    minBitsForValid, ok := t.minValidMap["` + name + `"]
	if !ok {
		minBitsForValid = minlength
	}
	if len(payload)-int(*offset) < int(minBitsForValid) {
		if optional {
			return nil
		}
		log.Println("ERROR: -1 PositionReport too short")
		return nil
	}
	var num int
	var str string
    `
				for _, f := range fields {
					if f.embedded {
						output += `
	p.` + f.typ + ` = parse` + f.typ + `(payload, t, offset)
`
					} else if f.isArray {
						switch f.typ {
						case "AssignedModeCommandData":
						case "BinaryAcknowledgeData":
						case "DataLinkManagementMessageData":
							output += `var elems = make([]` + f.typ + `, ` + strconv.Itoa(f.arrayLength) + `)
`
							for i := 0; i < f.arrayLength; i++ {
								output += `elems[` + strconv.Itoa(i) + `] = parse` + f.typ + `(payload, t, offset)
`
							}
							output += `p.` + f.name + ` = elems`
						case "byte":
							output += `p.` + f.name + ` = payload[*offset:*offset+` + strconv.Itoa(f.arrayLength) + `]
	*offset += ` + strconv.Itoa(f.arrayLength) + `
`
						}
					} else {
						// simple type parsing
						switch f.typ {
						case "ChannelManagementUnicastData":
						case "ChannelManagementBroadcastData":
						case "InterrogationStation2":
						case "InterrogationStation1Message1":
						case "InterrogationStation1Message2":
						case "StaticDataReportA":
						case "StaticDataReportB":
						case "FieldApplicationIdentifier":
						case "FieldETA":
						case "FieldDimension":
							output += `p.` + f.name + ` = parseFieldDimension(patload, t, offset)
`
						case "FieldLatLonFine":
							output += `num = extractNumber(payload, true, offset, ` + strconv.Itoa(f.width) + `)
	if if !t.FloatWithoutConversion {
		p.` + f.name + ` = FieldLatLonFine(num) / 10000 / 60
	} else {
		p.` + f.name + ` = FieldLatLonFine(num)
	}
`
						case "FieldLatLonCoarse":
							output += `num = extractNumber(payload, true, offset, ` + strconv.Itoa(f.width) + `)
	if if !t.FloatWithoutConversion {
		p.` + f.name + ` = FieldLatLonCoarse(num) / 10 / 60
	} else {
		p.` + f.name + ` = FieldLatLonCoarse(num)
	}
`
						case "Field10":
							output += `num = extractNumber(payload, true, offset, ` + strconv.Itoa(f.width) + `)
	if if !t.FloatWithoutConversion {
		p.` + f.name + ` = Field10(num) / 10
	} else {
		p.` + f.name + ` = Field10(num)
	}
`
						case "bool":
							output += `num = extractNumber(payload, true, offset, ` + strconv.Itoa(f.width) + `)
	p.` + f.name + ` = num == 1`
						case "int16":
							output += `num = extractNumber(payload, true, offset, ` + strconv.Itoa(f.width) + `)
	p.` + f.name + ` = int16(num)
`
						case "string":
							output += `str = extractString(payload, offset, ` + strconv.Itoa(f.width) + `, t.DropSpace)
	p.` + f.name + ` = str
`
						case "uint16":
							output += `num = extractNumber(payload, false, offset, ` + strconv.Itoa(f.width) + `)
	p.` + f.name + ` = uint16(num)
`
						case "uint32":
							output += `num = extractNumber(payload, false, offset, ` + strconv.Itoa(f.width) + `)
	p.` + f.name + ` = uint32(num)
`
						case "uint8":
							output += `num = extractNumber(payload, false, offset, ` + strconv.Itoa(f.width) + `)
	p.` + f.name + ` = uint8(num)
`
						default:
							panic("unhandled type: " + f.typ)
						}
					}

				}
				output += `
	return p
}

`
			}
		}
	}

	// now output back to a file
	outputPath := path.Join(*outputDir, "codec_gen.go")
	err = os.WriteFile(outputPath, []byte(output), 0644)
	if err != nil {
		panic(err)
	}
	log.Println("generated: ", outputPath)
}
